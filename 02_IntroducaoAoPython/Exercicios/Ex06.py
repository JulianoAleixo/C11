import math

number = float(input("Entre com um número decimal: "))

print(f"A raíz quadrada de {number} é {math.sqrt(number)}")
print(f"A função teto de {number} é {math.ceil(number)}")
print(f"A função chão de {number} é {math.floor(number)}")
print(f"A parte inteira de {number} é {math.trunc(number)}")
